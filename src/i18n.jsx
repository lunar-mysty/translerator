export const UI_LOCALES = Object.entries(translations).map(([code, t]) => ({
  code,
  label: t.label,
}))

const I18nContext = createContext()

export function I18nProvider({ children }) {
  const [locale, setLocale] = useState(() => {
    const saved = localStorage.getItem('translerator-locale')
    return saved && translations[saved] ? saved : 'en'
  })

  const changeLocale = useCallback((code) => {
    setLocale(code)
    localStorage.setItem('translerator-locale', code)
    document.documentElement.lang = code
    document.documentElement.dir = code === 'ar' ? 'rtl' : 'ltr'
  }, [])

  useEffect(() => {
    document.documentElement.lang = locale
    document.documentElement.dir = locale === 'ar' ? 'rtl' : 'ltr'
  }, [locale])

  const t = translations[locale]

  return (
    <I18nContext.Provider value={{ locale, changeLocale, t }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}
